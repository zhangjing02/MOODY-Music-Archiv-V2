/**
 * 获取歌曲的 LRC 歌词
 * @param {string} artist - 艺术家名称
 * @param {string} song - 歌曲名称
 * @returns {string|null} LRC 格式的歌词文本，如果未找到返回 null
 */
function getLrcLyrics(artist, song) {
    const key = `${artist}-${song}`;
    return LYRICS_LRC_DB[key] || null;
}

/**
 * 添加新的 LRC 歌词到数据库
 * @param {string} artist - 艺术家名称
 * @param {string} song - 歌曲名称
 * @param {string} lrcText - LRC 格式的歌词文本
 */
function addLrcLyrics(artist, song, lrcText) {
    const key = `${artist}-${song}`;
    LYRICS_LRC_DB[key] = lrcText;
}

/**
 * 获取所有已存储的歌词键列表
 * @returns {string[]} 所有歌词键的数组
 */
function getAllLyricsKeys() {
    return Object.keys(LYRICS_LRC_DB);
}

